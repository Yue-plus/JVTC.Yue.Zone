package zone.yue.jvtc.solution.ooptc.work8.q5;

public interface Shape {
    /**
     * @return 获取该形状的面积
     */
    double getArea();

    /**
     * @return 获取该形状的周长
     */
    double getPerimeter();

    /**
     * @return 获取该形状的表面积
     */
    double getSurfaceArea();

    /**
     * @return 获取该形状的体积
     */
    double getVolume();
}
